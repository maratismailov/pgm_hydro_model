#!env/bin/python
"""
Расчетно-лабораторная работа 3 (тема лекции 3)
Расчет параметров парной линейной регрессии и корреляции
Аналог программы ЛИНЕЙН Excel

Данные: stations2.csv — 33 МС Кыргызстана
y  = температура выбранного месяца (или года)
x  = высота Z (км)

Выходная форма (Таблица 1):
  ┌──────────┬──────────┐
  │  b1      │  b0      │
  │  Sb1     │  Sb0     │
  │  r²      │  S²      │
  │  F       │  k2      │
  │  Σ1      │  Σ2      │
  └──────────┴──────────┘
"""

import numpy as np
import pandas as pd
from pathlib import Path
from scipy import stats

# ─── Загрузка данных ──────────────────────────────────────────────────────────
csv_path = Path(__file__).parent / "stations2.csv"
df = pd.read_csv(csv_path)

MONTHS = {
    "I":    "Январь",   "II":   "Февраль", "III":  "Март",
    "IV":   "Апрель",   "V":    "Май",      "VI":   "Июнь",
    "VII":  "Июль",     "VIII": "Август",   "IX":   "Сентябрь",
    "X":    "Октябрь",  "XI":   "Ноябрь",   "XII":  "Декабрь",
    "year": "Год",
}

# ─── Выбор периода ────────────────────────────────────────────────────────────
print("Доступные периоды:")
for key, name in MONTHS.items():
    print(f"  {key:>5} — {name}")

period = input("\nВведите обозначение периода (например, VII или year): ").strip()
if period not in MONTHS:
    print(f"Период '{period}' не найден. Используется 'year'.")
    period = "year"

print(f"\n>>> Выбран период: {MONTHS[period]}\n")

# ─── Данные ───────────────────────────────────────────────────────────────────
names = df["station"].tolist()
x = df["z_km"].to_numpy()        # предиктор — высота Z
y = df[period].to_numpy()        # отклик — температура
n = len(y)

# ─── Вычисление статистик (аналог ЛИНЕЙН) ────────────────────────────────────
b1, b0, r, p_value, sb1 = stats.linregress(x, y)

y_pred    = b0 + b1 * x
residuals = y - y_pred

SS1 = np.sum((y_pred - y.mean()) ** 2)   # Σ1 — объяснённая сумма квадратов
SS2 = np.sum(residuals ** 2)             # Σ2 — остаточная сумма квадратов
k2  = n - 2                              # степени свободы
s2  = SS2 / k2                           # дисперсия остатков S²
s   = np.sqrt(s2)                        # стандартная ошибка регрессии
r2  = r ** 2                             # коэффициент детерминации r²
F   = SS1 / s2                           # F-критерий

x_mean = x.mean()
sb0 = s * np.sqrt(1/n + x_mean**2 / np.sum((x - x_mean)**2))

SEP  = "═" * 50
sep2 = "─" * 50

# ─── Таблица 1 (форма ЛИНЕЙН) ─────────────────────────────────────────────────
print(SEP)
print(f"  ПАРНАЯ ЛИНЕЙНАЯ РЕГРЕССИЯ — {MONTHS[period].upper()}")
print(f"  y = b0 + b1·Z    (температура от высоты)")
print(SEP)

print(f"""
  Таблица 1 — ЛИНЕЙН
  ┌{'─'*22}┬{'─'*22}┐
  │  {'A':<20}│  {'B':<20}│
  ├{'─'*22}┼{'─'*22}┤
  │  b1  = {b1:>12.3f}    │  b0  = {b0:>12.3f}    │
  │  Sb1 = {sb1:>12.3f}    │  Sb0 = {sb0:>12.3f}    │
  │  r²  = {r2:>12.3f}    │  S²  = {s2:>12.3f}    │
  │  F   = {F:>12.3f}    │  k2  = {k2:>12.3f}    │
  │  Σ1  = {SS1:>12.3f}    │  Σ2  = {SS2:>12.3f}    │
  └{'─'*22}┴{'─'*22}┘""")

# ─── Дополнительная интерпретация ────────────────────────────────────────────
print(f"\n  Уравнение регрессии:")
sign = "+" if b0 >= 0 else "−"
print(f"    T = {b1:.4f}·Z {sign} {abs(b0):.4f}")

print(f"\n  Интерпретация:")
strength = "сильная" if abs(r) > 0.7 else ("умеренная" if abs(r) > 0.4 else "слабая")
direction = "обратная" if r < 0 else "прямая"
print(f"    r  = {r:.4f}  → связь {strength}, {direction}")
print(f"    r² = {r2:.4f}  → модель объясняет {r2*100:.1f}% дисперсии температуры")
print(f"    S  = {s:.4f}°C  — стандартная ошибка регрессии")
print(f"    p  = {p_value:.2e}  → {'значима (p < 0.05)' if p_value < 0.05 else 'незначима'}")

# ─── Таблица предсказаний ─────────────────────────────────────────────────────
print(f"\n{SEP}")
print(f"  РАСЧЁТНЫЕ ЗНАЧЕНИЯ ПО 33 МС")
print(SEP)
print(f"\n  {'Станция':<22} {'Z,км':>6} {'T факт':>8} {'T расч':>8} {'Остаток':>9}")
print(f"  {'─'*55}")
for name, xi, yi, yi_pred in zip(names, x, y, y_pred):
    print(f"  {name:<22} {xi:>6.3f} {yi:>8.1f} {yi_pred:>8.2f} {yi - yi_pred:>9.2f}")

rmse = np.sqrt(np.mean((y - y_pred) ** 2))
print(f"\n  RMSE = {rmse:.4f}°C")
print(f"\n{SEP}")
