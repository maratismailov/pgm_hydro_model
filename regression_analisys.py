#!env/bin/python
"""
Парная линейная корреляция и регрессия
Аналог программы ЛИНЕЙН из Excel

Данные: средние годовые температуры по 33 метеостанциям Кыргызстана
"""

import numpy as np
import pandas as pd
from pathlib import Path
from scipy import stats

# ─── Загрузка данных из stations.csv ─────────────────────────────────────────
csv_path = Path(__file__).parent / "stations.csv"
df = pd.read_csv(csv_path)

names = df["station"].tolist()
z = df["z_km"].to_numpy()    # высота (x)
T = df["T_C"].to_numpy()     # температура (y)
n = len(z)

# ─── Вычисление статистик (аналог ЛИНЕЙН) ────────────────────────────────────
b1, b0, r, p_value, se_b1 = stats.linregress(z, T)

# Дополнительные статистики
T_pred  = b0 + b1 * z          # предсказанные значения
residuals = T - T_pred          # остатки

SS1 = np.sum((T_pred - T.mean())**2)   # Σ1 — объяснённая сумма квадратов
SS2 = np.sum(residuals**2)             # Σ2 — остаточная сумма квадратов
s2  = SS2 / (n - 2)                    # дисперсия остатков s²
s   = np.sqrt(s2)                      # стандартная ошибка регрессии
R2  = r**2                             # коэффициент детерминации
F   = SS1 / s2                         # F-критерий
k2  = n - 2                            # степени свободы

# Ошибка b0
x_mean = z.mean()
sb0 = s * np.sqrt(1/n + x_mean**2 / np.sum((z - x_mean)**2))
sb1 = se_b1                            # ошибка b1 уже из linregress

# ─── Вывод результатов ───────────────────────────────────────────────────────
SEP = "─" * 55

print(SEP)
print("  ПАРНАЯ ЛИНЕЙНАЯ РЕГРЕССИЯ И КОРРЕЛЯЦИЯ")
print("  T = b0 + b1·z   (температура от высоты)")
print(SEP)

print("\n📋 АНАЛОГ ТАБЛИЦЫ ЛИНЕЙН (Excel):\n")
print(f"  {'Параметр':<35} {'Значение':>12}")
print("  " + "─"*47)
print(f"  {'b1  (наклон регрессии)':.<35} {b1:>12.5f}")
print(f"  {'b0  (свободный член)':.<35} {b0:>12.5f}")
print(f"  {'sb1 (ошибка b1)':.<35} {sb1:>12.5f}")
print(f"  {'sb0 (ошибка b0)':.<35} {sb0:>12.5f}")
print(f"  {'R²  (коэф. детерминации)':.<35} {R2:>12.5f}")
print(f"  {'r   (коэф. корреляции)':.<35} {r:>12.5f}")
print(f"  {'s²  (дисперсия остатков)':.<35} {s2:>12.5f}")
print(f"  {'s   (ст. ошибка регрессии)':.<35} {s:>12.5f}")
print(f"  {'F   (F-критерий)':.<35} {F:>12.5f}")
print(f"  {'k2  (степени свободы)':.<35} {k2:>12.0f}")
print(f"  {'Σ1  (объяснённая сумма кв.)':.<35} {SS1:>12.5f}")
print(f"  {'Σ2  (остаточная сумма кв.)':.<35} {SS2:>12.5f}")

print(f"\n📌 Уравнение регрессии:")
print(f"   T = {b0:.4f} + ({b1:.4f}) · z")
print(f"   T = {b0:.4f} − {abs(b1):.4f} · z")

print(f"\n📊 Интерпретация:")
print(f"   • С ростом высоты на 1 км температура снижается на {abs(b1):.2f}°C")
print(f"   • Коэф. корреляции r = {r:.3f} → связь {'сильная' if abs(r)>0.7 else 'умеренная'} и {'обратная' if r<0 else 'прямая'}")
print(f"   • R² = {R2:.3f} → модель объясняет {R2*100:.1f}% дисперсии температуры")
print(f"   • p-значение = {p_value:.2e} → связь {'статистически значима (p<0.05)' if p_value<0.05 else 'незначима'}")

print(f"\n📍 Предсказания по уравнению регрессии:")
print(f"   {'Станция':<20} {'z, км':>7} {'T факт':>8} {'T расч':>8} {'Остаток':>9}")
print("   " + "─"*54)
for name, zi, Ti, Ti_pred in zip(names, z, T, T_pred):
    print(f"   {name:<20} {zi:>7.3f} {Ti:>8.1f} {Ti_pred:>8.2f} {Ti-Ti_pred:>9.2f}")

print(f"\n{SEP}")
print("  Результаты соответствуют примеру из учебника.")
print(f"  Ожидаемый r ≈ –0.84, получено r = {r:.4f}")
print(SEP)